

#ifndef _TYPEDEFINE_H_
#define _TYPEDEFINE_H_

//*****************************************************************************************************************
typedef unsigned char           BOOLEAN;                                      /*           1 bit quantity        */
typedef unsigned char           INT8U, u8, U8, uint8,  UINT8, BYTE;           /* Unsigned  8 bit quantity        */
typedef signed   char           INT8S, s8, S8, int8,  INT8;                   /* Signed    8 bit quantity        */
typedef unsigned short int      INT16U, u16, U16, uint16, UINT16, WORD;       /* Unsigned 16 bit quantity        */
typedef signed short int        INT16S, s16, S16, int16, INT16;               /* Signed   16 bit quantity        */
typedef unsigned int            INT32U, u32, U32, uint32, UINT32, DWORD;      /* Unsigned 32 bit quantity        */
typedef signed   int            INT32S, s32, S32, int32, INT32;               /* Signed   32 bit quantity        */
typedef float                   FP32;                                         /* Single precision floating point */
typedef double                  FP64;                                         /* Double precision floating point */
//*****************************************************************************************************************

#endif

